#Tempo de serviço e bônus
Tserviço = float(input("Digite o seu tempo de serviço em anos: "))
if Tserviço > 5:
    Salário = float(input("Digite o valor do seu salário: "))
    Bônus = Salário*0.05 
    print(f"Seus bônus é: {Bônus}")
else:
    print("Tempo insuficiente para receber bônus :(")


    
    
